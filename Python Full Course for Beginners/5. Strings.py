#Förklaring:

# course är namnet på variabeln.
# = används för att tilldela ett värde till variabeln.
# 'Python's for Beginners' är strängvärdet som tilldelas variabeln.

course = "Python's for Beginners"  # Korrekt med enkla citattecken
print(course)
course = 'Python"s for Beginners'  # Korrekt med dubbla citattecken
print(course)
course = 'Python"s for "Beginners"' # Korrekt med betoning på dubbla citattecken
print(course)
course = '''
Hi John,
Here is our first email to you.
Thank you
'''
print(course)