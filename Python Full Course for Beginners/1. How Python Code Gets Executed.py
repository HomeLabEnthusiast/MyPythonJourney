# Förklaring av print()-funktionen:

# print()-funktionen är en grundläggande funktion i Python som används för att visa text eller värden på skärmen.
# Den kan ta en eller flera argument (strängar, variabler, etc.) som ska skrivas ut.

print('Tomas Kilic') # Skriver ut strängen "Tomas Kilic"
print('x' * 10) # Skriver ut bokstav x 10 gånger
print("Summan är:", 5 + 3)  # Skriver ut strängen "Summan är:" och beräknar 5 + 3
